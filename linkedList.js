/**
 * @name  linkedListGenerator
 * @description  Main Module
 * @return {Object} an object exposing methods to be used to manipulate a linked list
 */
function linkedListGenerator(){
		var head = null;
		var tail = null;

		function getHead(){
			return head;
		}

		function getTail(){
			return tail;
		}

		function add(Value){
			return value;
		}

		return{
			getHead: getHead,
			getTail: getTail,
			add:add,
		};

}